function [ eps,dfi,edot ] = Nuta( y,m,d,hour,min,sec)
%evaluates the nutation values
%Inputs : year,month,day,hour,min,sec
JD=floor(365.25*y)+floor(30.6001*(m+1))+d+hour/24+1720981.5;
T=(JD-2451545)/36525;
d=JD-2451545;
mult = 10^-4/3600;
eps=23.4393-(0.0000004*d);
file=load('nut1980.dat');
x =[(485866.733/3600)+(1325360+(715922.633/3600))*(T)+(31.310/3600)*(T^2)+(0.064/3600)*(T^3),
        (1287099.804/3600)+(99360+(1292581.224/3600))*(T)-(0.577/3600)*(T^2)-(0.012/3600)*(T^3),
        (335778.877/3600)+(1342360+(295263.137/3600))*(T)-(13.257/3600)*(T^2)+(0.011/3600)*(T^3),
       (1072261.307/3600)+(1236360+(1105601.328/3600))*(T)-(6.891/3600)*(T^2)+(0.019/3600)*(T^3),
       (450160.280/3600)-(5360+(482890.539/3600))*(T)+(7.455/3600)*(T^2)+(0.008/3600)*(T^3)];
y =0 ;
for j = 1:106
    for i = 1:5
        y =y+  (x(i)*file(j,i));      
    end
dfi= ((file(1,7)*mult)+(file(1,8)*mult)*T)*sin(y);
edot= ((file(1,9)*mult)+(file(1,10)*mult)*T)*cos(y);
end
result=[eps;dfi;edot];

end

